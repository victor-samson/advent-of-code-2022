type HandShape = "rock" | "paper" | "scissors";

const getHandShape = (code: string): HandShape => {
  switch (code.toLowerCase()) {
    case "a":
    case "x":
      return "rock";
    case "b":
    case "y":
      return "paper";
    case "c":
    case "z":
      return "scissors";
    default:
      throw Error(`Unknown hand shape: ${code}`);
  }
};

const getHandShapeScore = (handShape: HandShape) => {
  switch (handShape) {
    case "rock":
      return 1;
    case "paper":
      return 2;
    case "scissors":
      return 3;
  }
};

const getRoundScore = (you: HandShape, opponent: HandShape) => {
  // Draw.
  if (you === opponent) return 3;

  // You won.
  if (
    (you === "rock" && opponent === "scissors") ||
    (you === "scissors" && opponent === "paper") ||
    (you === "paper" && opponent === "rock")
  )
    return 6;

  // You lost.
  return 0;
};

const result = input
  .split(/\n/)
  .map((v) => v.split(" "))
  .reduce<number>((acc, curr) => {
    const you = getHandShape(curr[1]);
    const opponent = getHandShape(curr[0]);

    const handShapeScore = getHandShapeScore(you);
    const roundScore = getRoundScore(you, opponent);

    return acc + handShapeScore + roundScore;
  }, 0);

